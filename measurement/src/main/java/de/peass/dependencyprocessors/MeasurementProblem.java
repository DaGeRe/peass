package de.peass.dependencyprocessors;

class MeasurementProblem {
   String reason;

   public MeasurementProblem() {
      super();
   }

   public MeasurementProblem(String reason) {
      super();
      this.reason = reason;
   }

   public String getReason() {
      return reason;
   }

   public void setReason(String reason) {
      this.reason = reason;
   }
}