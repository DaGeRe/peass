package de.dagere.peass.testtransformation;

public class JUnitVersions {
	private boolean junit3;
	private boolean junit4;
	private boolean junit5;

	public boolean isJunit3() {
		return junit3;
	}

	public void setJunit3(boolean junit3) {
		this.junit3 = junit3;
	}

	public boolean isJunit4() {
		return junit4;
	}

	public void setJunit4(boolean junit4) {
		this.junit4 = junit4;
	}

	public boolean isJunit5() {
		return junit5;
	}

	public void setJunit5(boolean junit5) {
		this.junit5 = junit5;
	}

}
